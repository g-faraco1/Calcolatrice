package com.example.calcolatrice;

public class calcolatrice {

    private int a, b;

    public calcolatrice() {
        this.a = 0;
        this.b = 0;
    }

    public int somma(int a, int b){
        return a+b;
    }

    public int moltiplicazione(int a, int b){
        return a*b;
    }

}
